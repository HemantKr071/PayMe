const mongoose = require("mongoose");
const { number } = require("zod");
require('dotenv').config();
//const DATABASE_URL = `${process.env.DATABASE_URL}`;
mongoose.connect(`${process.env.DATABASE_URL}`);
const userSchema = new mongoose.Schema({
    username:String,
    password:String,
    firstname:String,
    lastname:String
});
const User = mongoose.model("User",userSchema);

const accountSchema = new mongoose.Schema({
    userID :{
        type:mongoose.Schema.Types.ObjectID,  // reference to user model  type: mongoose.Schema.Types.ObjectId: T
                                            // this indicates that the userID field will store MongoDB ObjectIDs, which are unique identifiers automatically generated by MongoDB for each document.
        ref:User,
        required:true
    },
    balance :{
        type: Number,
        required:true
    }
})
const Account = mongoose.model("Account",accountSchema);

module.exports = {
    User,
    Account
};